import type {
  MatchRecord,
  MatchSetRecord,
  PlayerRecord,
  TournamentRecord,
} from "@/lib/tournament-types";

function createPlayers(
  tournamentId: string,
  names: string[],
  createdAt: string,
): PlayerRecord[] {
  return names.map((displayName, index) => ({
    id: `${tournamentId}-player-${index + 1}`,
    tournamentId,
    displayName,
    seed: index + 1,
    status: "active",
    createdAt,
  }));
}

function createSet(
  id: string,
  matchId: string,
  setNo: number,
  player1Score: number,
  player2Score: number,
  updatedAt: string,
  note: string | null = null,
): MatchSetRecord {
  return {
    id,
    matchId,
    setNo,
    player1Score,
    player2Score,
    note,
    updatedAt,
  };
}

function createMatch(
  match: Omit<MatchRecord, "sets"> & { sets: MatchSetRecord[] },
): MatchRecord {
  return match;
}

